public class ClassifyListAdapter extends BaseQuickAdapter<MenuEntity, BaseViewHolder> {

	public ClassifyListAdapter(@Nullable List<MenuEntity> data) {
		super(R.layout.item_classify_list, data);
	}

	@Override
	protected void convert(BaseViewHolder helper, MenuEntity item) {
		Glide.with(mContext).load(item.getIconUrl()).crossFade().into((ImageView) helper.getView(R.id.iv_item_album));
		helper.setText(R.id.tv_item_name, item.getName());
	}
}
