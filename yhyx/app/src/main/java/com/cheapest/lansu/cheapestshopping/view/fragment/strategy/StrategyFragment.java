@SuppressLint("ValidFragment")
public class StrategyFragment extends Fragment {

	@Bind(R.id.iv_gonglue_bg)
	ImageView iv_gonglue_bg;

	int index = 0;

	public StrategyFragment(int index) {
		this.index = index;
	}

	@Nullable
	@Override
	public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
		View view = inflater.inflate(R.layout.fragment_layout_strategy, container, false);
		ButterKnife.bind(this, view);
		iv_gonglue_bg.setImageResource(R.mipmap.iv_gonglue_bg_01 + index);
		return view;
	}

	@Override
	public void onDestroyView() {
		super.onDestroyView();
		ButterKnife.unbind(this);
	}
}
